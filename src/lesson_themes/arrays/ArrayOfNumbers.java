package lesson_themes.arrays;

public class ArrayOfNumbers {
    public static void main(String[] args) {
        int n = -10;
        while(n<26){
            System.out.print(n+", ");
            n++;
        }
        System.out.println(" \n");
        for(int nya = -10; nya<26; nya++){
            System.out.println(nya);
        }

    }
}
